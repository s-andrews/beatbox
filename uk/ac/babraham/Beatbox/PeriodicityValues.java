package uk.ac.babraham.Beatbox;

public class PeriodicityValues {

	public float periodicity;
	public float meanIntensityDiff;
	public float stdev;
	
	
	public PeriodicityValues(float periodicity, float meanIntensityDiff, float stdev) {
		this.periodicity = periodicity;
		this.meanIntensityDiff = meanIntensityDiff;
		this.stdev = stdev;
	}
}
